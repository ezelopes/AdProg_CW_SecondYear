    /**
     * Checks the conditions of the order and enables or disables buttons.
     *
     * @author Lee 750834
     */
    public void runtimeValidateAddOrderButtons() {
        try {
            String boxQuantityString = quantityText.getText();
            String widthString = widthText.getText();
            String heightString = heightText.getText();
            String lengthSrting = lengthText.getText();
            if (!boxQuantityString.isEmpty() && !widthString.isEmpty() && !heightString.isEmpty() && !lengthSrting.isEmpty()) {
                int boxQuantity = Integer.parseInt(quantityText.getText());
                if (listCardboards.size() >= 20 || boxQuantity == 0 || boxQuantity > 100 || !controlSizes(lengthSrting) || !controlSizes(heightString) || !controlSizes(widthString)) {
                    jButtonAddOrder.setEnabled(false);
                } else {
                    jButtonAddOrder.setEnabled(true);
                }
            }
            else jButtonAddOrder.setEnabled(false);
        } catch (Exception error) {
            quantityText.setText("1");
            String errorText = "Ensure the quantity input is not empty or is a decimal number";
            displayErrorModal("Invalid Box Inputs", errorText + " " + error);
        }
        
    }
    
    public boolean controlSizes(String currentSize){
        boolean valid = false;
        double sizeValue = Double.parseDouble(currentSize);
        if (sizeValue  >= 0.5 && sizeValue <= 10 ) valid = true;
        return valid;
    }

