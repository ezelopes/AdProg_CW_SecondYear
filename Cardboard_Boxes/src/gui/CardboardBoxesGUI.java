    private void jButtonAddOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddOrderActionPerformed
        // TODO add your handling code here:
        try{
            double width = Double.parseDouble(widthText.getText()); 
            double length = Double.parseDouble(lengthText.getText()); 
            double height = Double.parseDouble(heightText.getText());
            String grade = String.valueOf(jComboBoxGrade.getSelectedIndex() + 1); //make sure selected index is zero in loading
            String colour = ""; 
            for (Enumeration<AbstractButton> buttons = BoxPrintingGroup.getElements(); buttons.hasMoreElements();) {
                AbstractButton button = buttons.nextElement();
                if (button.isSelected()) {
                    colour = button.getName();
                }
            }
            boolean reBottom = false; 
            boolean reCorner = false;
            boolean sealableTop = false;
            if(bottomCheckBox.isSelected()) reBottom = true;
            if(cornerCheckBox.isSelected()) reCorner = true;
            if(sealableCheckBox.isSelected()) sealableTop = true;
            Integer quantity = Integer.parseInt(quantityText.getText());
            String type = "Not valid";

            ArrayList userInput = new ArrayList();
            userInput.add(colour);
            userInput.add(reBottom);
            userInput.add(reCorner);

            for (gui.Type currentType : gui.Type.values()) { 
                 type = verifyType(currentType, userInput, grade);
                 if(type != "Not valid")
                     break;
            }

            switch(type){
                case "ONE":{
                    Type1 cardboard1 = new Type1(width, length, height, grade, sealableTop, quantity);
                    listCardboards.add(cardboard1);
                    break;
                }
                case "TWO":{
                    Type2 cardboard2 = new Type2(width, length, height, grade, colour, sealableTop, quantity);   
                    listCardboards.add(cardboard2);
                    break;
                }
                case "THREE":{
                    Type3 cardboard3 = new Type3(width, length, height, grade, colour, sealableTop, quantity);     
                    listCardboards.add(cardboard3);
                    break;
                }
                case "FOUR":{
                    Type4 cardboard4 = new Type4(width, length, height, grade, colour, reBottom, sealableTop, quantity); 
                    listCardboards.add(cardboard4);
                    break;
                }
                case "FIVE":{
                    Type5 cardboard5 = new Type5(width, length, height, grade, colour, reBottom, reCorner, sealableTop, quantity);      
                    listCardboards.add(cardboard5);
                    break;
                }
                default:{ 
                    JOptionPane.showMessageDialog(null, "Type not valid!");
                    break;
                }
            }
            if(type != "Not valid"){ //update total cost and anything else function
                counter++;
                basketModel.add(counter, "First order: " + listCardboards.get(counter).getInformation());
                finalCost += listCardboards.get(counter).getCost();//to be changed maybe
                jTextFieldTotalOrders.setText(String.valueOf(listCardboards.size()));
                jTextFieldTotalCost.setText("Â£" + String.format("%.2f", finalCost));
            }
        }
        catch(Exception ex ){ JOptionPane.showMessageDialog(null, ex.toString()); }
    }//GEN-LAST:event_jButtonAddOrderActionPerformed

